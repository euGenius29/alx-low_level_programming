#include "3-calc.h"

/**
 * op_add - adds ints
 * @a: first int
 * @b: second int
 * @s: operator
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts ints
 * @a: first int
 * @b: second int
 * @s: operator
 * Return: difference
 */
int op_sub(int a, int b)
{
        return (a - b);
}
/**
 * op_mul - multiplies ints
 * @a: first int
 * @b: second int
 * @s: operator
 * Return: multiple
 */
int op_mul(int a, int b)
{
        return (a * b);
}
/**
 * op_div - divides ints
 * @a: first int
 * @b: second int
 * @s: operator
 * Return: dividend
 */
int op_div(int a, int b)
{
        return (a / b);
}
/**
 * op_add - finds remainder
 * @a: first int
 * @b: second int
 * @s: operator
 * Return: remainder
 */
int op_add(int a, int b)
{
        return (a % b);
}
